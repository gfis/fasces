#!perl

# skip lines when a longer one with same prefix follows
# @(#) $Id$
# 2019-09-11, Georg Fischer
#------------------------------------------------------
# Usage:
#   perl longer < infile > outfile
#--------------------------------------------------------
use strict;
use integer;
#----------------
my ($old, $new) = ("", "");
while (<>) {
    s{\s+\Z}{}; # chompr
    $new = $_;
    if ($old eq substr($new, 0, length($old))) {
        $old = "$new";
    } else {
        print "$old\n";
        $old = "$new";
    }
} # while <>
print "$old\n";
